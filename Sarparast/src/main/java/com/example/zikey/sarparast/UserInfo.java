public class UserInfo {
    private int image_user;
    private String code_user;
    private String name_user;
    private String execute_user;


    public int getImage_user() {
        return image_user;
    }

    public String getCode_user() {
        return code_user;
    }

    public String getName_user() {
        return name_user;
    }

    public String getExecute_user() {
        return execute_user;
    }

    public void setImage_user(int image_user) {
        this.image_user = image_user;
    }

    public void setCode_user(String code_user) {
        this.code_user = code_user;
    }

    public void setName_user(String name_user) {
        this.name_user = name_user;
    }

    public void setExecute_user(String execute_user) {
        this.execute_user = execute_user;
    }
}
